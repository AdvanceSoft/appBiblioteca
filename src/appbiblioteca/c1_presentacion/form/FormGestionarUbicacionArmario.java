     private void crearTabla(){
        Tabla tabla = new Tabla();
        tabla.agregarColumna(new Columna("CODIGO", "java.lang.Integer"));
        tabla.agregarColumna(new Columna("NOMBRE", "java.lang.String"));
        ModeloTabla modeloTablaProveedor = new ModeloTabla(tabla);
        tablaUbicacionArmario.setModel(modeloTablaProveedor);
        TableColumn columna0,columna1;
        //CODIGO
        columna0 = tablaUbicacionArmario.getColumnModel().getColumn(0);
        tablaUbicacionArmario.removeColumn(columna0);
        //NOMBRE
        columna1 = tablaUbicacionArmario.getColumnModel().getColumn(0);
        columna1.setPreferredWidth(416);
        columna1.setMaxWidth(416);
        columna1.setMinWidth(416);       
    }
    
    private void ponerFocoConSeleccionEnBuscar(){
        textoBuscar.selectAll();
        textoBuscar.requestFocus();
    }
    
    private void buscarUbicacionArmario(){
        ModeloTabla modeloTablaProveedor = (ModeloTabla) tablaUbicacionArmario.getModel();
        modeloTablaProveedor.eliminarTotalFilas();
        try{
            GestionarUbicacionArmarioServicio gestionarUbicacionArmarioServicio = new GestionarUbicacionArmarioServicio();        
            List<UbicacionArmario> listaUbicacionArmario = gestionarUbicacionArmarioServicio.buscar(textoBuscar.getText().trim().toUpperCase());
            if(listaUbicacionArmario!=null && listaUbicacionArmario.size() > 0){
                for (UbicacionArmario ubicacionArmario : listaUbicacionArmario) {
                    Fila fila = new Fila();
                    fila.agregarValorCelda(ubicacionArmario.getCodigo());
                    fila.agregarValorCelda(ubicacionArmario.getNombre());
                    modeloTablaProveedor.agregarFila(fila);
                }
                modeloTablaProveedor.refrescarDatos();
                ponerFocoConSeleccionEnBuscar();                
            }else{
                Mensaje.Mostrar_MENSAJE_NOSEENCONTRONINGUNRESULTADO(this);
                ponerFocoConSeleccionEnBuscar();
            }
        }catch(Exception e){
            Mensaje.mostrarErrorExcepcion(this, e.getMessage());
            ponerFocoConSeleccionEnBuscar();
        }
    }

    private UbicacionArmario obtenerObjetoDeLaTabla(){
        UbicacionArmario ubicacionArmario = null;
        int numFila = tablaUbicacionArmario.getSelectedRow();
        if(numFila >= 0){//verificamos si se selecciono alguna fila de la Tabla            
            GestionarUbicacionArmarioServicio gestionarUbicacionArmarioServicio = new GestionarUbicacionArmarioServicio();
            ModeloTabla modeloTabla = (ModeloTabla) tablaUbicacionArmario.getModel();
            int codigo = (int) modeloTabla.getValueAt(numFila, 0);
            try{
                ubicacionArmario = gestionarUbicacionArmarioServicio.buscar(codigo);
            }catch(Exception e){
                Mensaje.mostrarErrorExcepcion(this,e.getMessage());
            }
        }else
            Mensaje.Mostrar_MENSAJE_FILANOSELECCIONADO(this);
        return ubicacionArmario;
    }    

