    public void crearTabla(){
        Tabla tabla = new Tabla();
        tabla.agregarColumna(new Columna("CODIGO","java.lang.Integer"));
        tabla.agregarColumna(new Columna("NOMBRE","java.lang.String"));
        ModeloTabla modeloTablaUbicacionFila = new ModeloTabla(tabla);
        tablaUbicacionFila.setModel(modeloTablaUbicacionFila);
        TableColumn columna0,columna1,columna2;
        //CODIGO
        columna0 = tablaUbicacionFila.getColumnModel().getColumn(0);
        tablaUbicacionFila.removeColumn(columna0);
        //NOMBRE
        columna1 = tablaUbicacionFila.getColumnModel().getColumn(0);
        columna1.setPreferredWidth(416);
        columna1.setMaxWidth(416);
        columna1.setMinWidth(416);
    }
    
    private void ponerFocoConSeleccionEnBuscar(){
        textoBuscar.selectAll();
        textoBuscar.requestFocus();
    }
    
    private void buscarUbicacionFila(){
        ModeloTabla modeloTablaUbicacionFila = (ModeloTabla) tablaUbicacionFila.getModel();
        modeloTablaUbicacionFila.eliminarTotalFilas();
        try{
            GestionarUbicacionFilaServicio gestionarUbicacionFilaServicio = new GestionarUbicacionFilaServicio();        
            List<UbicacionFila> listaUbicacionFila = gestionarUbicacionFilaServicio.buscar(textoBuscar.getText().trim().toUpperCase());
            if(listaUbicacionFila!=null && listaUbicacionFila.size() > 0){
                for (UbicacionFila ubicacionFila : listaUbicacionFila) {
                    Fila fila = new Fila();
                    fila.agregarValorCelda(ubicacionFila.getCodigo());
                    fila.agregarValorCelda(ubicacionFila.getNombre());
                    modeloTablaUbicacionFila.agregarFila(fila);
                }
                modeloTablaUbicacionFila.refrescarDatos();
                ponerFocoConSeleccionEnBuscar();                
            }else{
                Mensaje.Mostrar_MENSAJE_NOSEENCONTRONINGUNRESULTADO(this);
                ponerFocoConSeleccionEnBuscar();
            }
        }catch(Exception e){
            Mensaje.mostrarErrorExcepcion(this, e.getMessage());
            ponerFocoConSeleccionEnBuscar();
        }
    }
    private UbicacionFila obtenerObjetoDeLaTabla(){
        UbicacionFila ubicacionFila = null;
        int numFila = tablaUbicacionFila.getSelectedRow();
        if(numFila >= 0){//verificamos si se selecciono alguna fila de la Tabla            
            GestionarUbicacionFilaServicio gestionarUbicacionFilaServicio = new GestionarUbicacionFilaServicio();
            ModeloTabla modeloTabla = (ModeloTabla) tablaUbicacionFila.getModel();
            int codigo = (int) modeloTabla.getValueAt(numFila, 0);
            try{
                ubicacionFila = gestionarUbicacionFilaServicio.buscar(codigo);
            }catch(Exception e){
                Mensaje.mostrarErrorExcepcion(this,e.getMessage());
            }
        }else
            Mensaje.Mostrar_MENSAJE_FILANOSELECCIONADO(this);
        return ubicacionFila;
    }    
