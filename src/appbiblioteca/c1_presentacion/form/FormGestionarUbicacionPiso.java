    private void crearTabla(){
        Tabla tabla = new Tabla();
        tabla.agregarColumna(new Columna("CODIGO", "java.lang.Integer"));
        tabla.agregarColumna(new Columna("NOMBRE", "java.lang.String"));
        ModeloTabla modeloTablaProveedor = new ModeloTabla(tabla);
        tablaUbicacionPiso.setModel(modeloTablaProveedor);
        TableColumn columna0,columna1;
        //CODIGO
        columna0 = tablaUbicacionPiso.getColumnModel().getColumn(0);
        tablaUbicacionPiso.removeColumn(columna0);
        //NOMBRE
        columna1 = tablaUbicacionPiso.getColumnModel().getColumn(0);
        columna1.setPreferredWidth(416);
        columna1.setMaxWidth(416);
        columna1.setMinWidth(416);       
    }
    
    private void ponerFocoConSeleccionEnBuscar(){
        textoBuscar.selectAll();
        textoBuscar.requestFocus();
    }
    
    private void buscarUbicacionPiso(){
        ModeloTabla modeloTablaProveedor = (ModeloTabla) tablaUbicacionPiso.getModel();
        modeloTablaProveedor.eliminarTotalFilas();
        try{
            GestionarUbicacionPisoServicio gestionarUbicacionPisoServicio = new GestionarUbicacionPisoServicio();        
            List<UbicacionPiso> lisUbicacionPiso = gestionarUbicacionPisoServicio.buscar(textoBuscar.getText().trim().toUpperCase());
            if(lisUbicacionPiso!=null && lisUbicacionPiso.size() > 0){
                for (UbicacionPiso ubicacionPiso : lisUbicacionPiso) {
                    Fila fila = new Fila();
                    fila.agregarValorCelda(ubicacionPiso.getCodigo());
                    fila.agregarValorCelda(ubicacionPiso.getNombre());
                    modeloTablaProveedor.agregarFila(fila);
                }
                modeloTablaProveedor.refrescarDatos();
                ponerFocoConSeleccionEnBuscar();                
            }else{
                Mensaje.Mostrar_MENSAJE_NOSEENCONTRONINGUNRESULTADO(this);
                ponerFocoConSeleccionEnBuscar();
            }
        }catch(Exception e){
            Mensaje.mostrarErrorExcepcion(this, e.getMessage());
            ponerFocoConSeleccionEnBuscar();
        }
    }

    private UbicacionPiso obtenerObjetoDeLaTabla(){
        UbicacionPiso ubicacionPiso = null;
        int numFila = tablaUbicacionPiso.getSelectedRow();
        if(numFila >= 0){//verificamos si se selecciono alguna fila de la Tabla            
            GestionarUbicacionPisoServicio gestionarUbicacionPisoServicio = new GestionarUbicacionPisoServicio();
            ModeloTabla modeloTabla = (ModeloTabla) tablaUbicacionPiso.getModel();
            int codigo = (int) modeloTabla.getValueAt(numFila, 0);
            try{
                ubicacionPiso = gestionarUbicacionPisoServicio.buscar(codigo);
            }catch(Exception e){
                Mensaje.mostrarErrorExcepcion(this,e.getMessage());
            }
        }else
            Mensaje.Mostrar_MENSAJE_FILANOSELECCIONADO(this);
        return ubicacionPiso;
    }    
