public class Correo {
    private int codigo;
    private String email;
    private boolean activo;

    public Correo() {
    }

    public Correo(int codigo, String email, boolean activo) {
        this.codigo = codigo;
        this.email = email;
        this.activo = activo;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public boolean isActivo() {
        return activo;
    }

    public void setActivo(boolean activo) {
        this.activo = activo;
    }
}