public class UbicacionFila {
    private int codigo;
    private String nombre;
    
    public UbicacionFila(){
        this.codigo = 0;
    }

    public UbicacionFila(int codigo, String nombre) {
        this.codigo = codigo;
        this.nombre = nombre;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}